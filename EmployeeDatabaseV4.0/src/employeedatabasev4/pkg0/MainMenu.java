    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) mainTable.getModel();
        model.setRowCount(0);
        mainTable.setModel(model);
    }
    
    public void addRowToTable(Object[] row) {
        DefaultTableModel model = (DefaultTableModel) mainTable.getModel();
        model.addRow(row);
        mainTable.setModel(model);   
    }
    

