    private void updateTableButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTableButtonActionPerformed
        // TODO add your handling code here:
        // model.insertRow(model.getRowCount(), new Object[]{"One", "Two", "Three", "Four", });
        ArrayList<EmployeeInfo> empList = EmployeeDatabase.getInstance().getAllEmployees();
        
        clearTable();
        
        for(EmployeeInfo emp : empList) {
            Object[] row = new Object[5];
            row[0] = String.valueOf(emp.getEmployeeNumber());
            row[1] = emp.getFirstName();
            row[2] = emp.getLastName();
            row[3] = emp.getWorkLocation();
            row[4] = emp.getDepartment();
            addRowToTable(row);
        }
    }//GEN-LAST:event_updateTableButtonActionPerformed

