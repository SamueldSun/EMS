    public void initInfo () {
        
        String employeeNumber = EmployeeDatabase.getInstance().getSearchEmployeeNumber();
        System.out.println("Emp Num: " + employeeNumber);
        ArrayList<EmployeeInfo> empList = EmployeeDatabase.getInstance().getSearchEmployees(employeeNumber, "Employee Number");
        
        for(EmployeeInfo emp : empList) {
            firstNameField.setText(emp.getFirstName());
            lastNameField.setText(emp.getLastName());
            dayBornField.setText(emp.getDayBorn());
            monthBornField.setText(emp.getMonthBorn());
            yearBornField.setText(emp.getYearBorn());
            
            employeeNumberField.setText(String.valueOf(emp.getEmployeeNumber()));
            workLocationField.setText(emp.getWorkLocation());
            departmentField.setText(emp.getDepartment());
            
            // typeWorkField.setText(emp.getTypeWork());
            switch (emp.getTypeWork().charAt(0)) {
                case 'P':
                    partTimeButton.setSelected(true);
                    fullTimeButton.setSelected(false);
                    break;
                case 'F':
                    partTimeButton.setSelected(false);
                    fullTimeButton.setSelected(true);
                    break;
                default:
                    System.out.println("RIP");
                    System.out.println(":" + emp.getTypeWork() + ":");
                    break;
            }
            
            
            hourlyWageField.setText(String.valueOf(emp.getHourlyWage()));
            hoursPerWeekField.setText(String.valueOf(emp.getHoursPerWeek()));
            weeksPerYearField.setText(String.valueOf(emp.getWeeksPerYear()));
            deductionRateField.setText(String.valueOf(emp.getDeductionRate()));
            yearlySalaryField.setText(String.valueOf(emp.getYearlySalary()));
        }
    }
    
    public void removeEmployee() {
        // Remove Employee
        ArrayList<EmployeeInfo> empList = EmployeeDatabase.getInstance().getSearchEmployees(EmployeeDatabase.getInstance().getSearchEmployeeNumber(), "Employee Number");
        
        for(EmployeeInfo emp : empList) {
            EmployeeDatabase.getInstance().removeEmployee(emp);
        }
    }
    
