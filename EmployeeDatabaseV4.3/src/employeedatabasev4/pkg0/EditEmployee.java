    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed

        removeEmployee();
        // Declares and reads field content
        // Wage Details
        double hourlyWage = 0;
        int hoursPerWeek = 0;
        int weeksPerYear = 0;
        double deductionRate = 0;
        double yearlySalary = 0;

        try {
            // String employeeStatus;
            // Personal Details
            String firstName = firstNameField.getText();
            String lastName = lastNameField.getText();
            String yearBorn = yearBornField.getText();
            String monthBorn = monthBornField.getText();
            String dayBorn = dayBornField.getText();

            // Work Details
            int employeeNumber = Integer.parseInt(employeeNumberField.getText());
            String workLocation = workLocationField.getText();
            String department = departmentField.getText();

            // Wage Details
            String typeWork = "";

            if (partTimeButton.isSelected()) {
                typeWork = "Part-time";
                hourlyWage = Double.parseDouble(hourlyWageField.getText());
                hoursPerWeek = Integer.parseInt(hoursPerWeekField.getText());
                weeksPerYear = Integer.parseInt(weeksPerYearField.getText());
                deductionRate = Double.parseDouble(deductionRateField.getText());
                yearlySalary = 0;

            } else if (fullTimeButton.isSelected()){
                typeWork = "Full-time";
                hourlyWage = 0;
                hoursPerWeek = 0;
                weeksPerYear = 0;
                deductionRate = Double.parseDouble(deductionRateField.getText());
                yearlySalary = Double.parseDouble(yearlySalaryField.getText());

            } else {
                // Add custom error message here later
            }

            EmployeeDatabase.getInstance().addEmployee(new NewEmployee(firstName, lastName, yearBorn, monthBorn, dayBorn, employeeNumber, workLocation, department, typeWork, hourlyWage, hoursPerWeek, weeksPerYear, deductionRate, yearlySalary));

            // Close Window
            this.dispose();
            System.out.println("Closed EditEmployee");

        } catch(IndexOutOfBoundsException|NumberFormatException e) {
            ErrorPage newWindow = new ErrorPage();
            newWindow.setVisible(true);
            System.out.println("Error occured! Opened ErrorPage");
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_submitButtonActionPerformed

